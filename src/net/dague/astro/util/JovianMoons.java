package net.dague.astro.util;

public class JovianMoons {
	public double jd;
	public double callisto;
	public double io;
	public double ganymede;
	public double europa;
	
	public JovianMoons()
	{}
	
	public String toString()
	{
		return "jd: " + jd + " - c: " + callisto + ", i: " + io + ", g: " + ganymede + ", e: " + europa;
	}
}
